# Casting Tipe Data(Merubah Tipe Data)
# Merubah dari satu tipe ke tipe lain

print('============Integer Data Type============')
data_int = 9
print('data = ', data_int, 'type = ', type(data_int))

#Casting to Float Data Type
print('Casting to Float Data Type')
data_float = float(data_int)
print('data = ', data_float, 'type = ', type(data_float))

#Casting to String Data Type
print('Casting to String Data Type')
data_str = str(data_int)
print('data = ', data_str, 'type = ', type(data_str))

#Casting to Boolean Data Type
print('Casting to Boolean Data Type')
data_bool = bool(data_int)
print('data = ', data_bool, 'type = ', type(data_bool))
#kenapa output nya true? karena nilai int nya bukan 0, jika int nya 0 maka output nya false.

